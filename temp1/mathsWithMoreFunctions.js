function multiplyNumbers(number1, number2) {
  return number1 * number2;
}

function divideNumbers(number1, number2) {
  return number1 / number2;
}

// Example usage:
const result1 = multiplyNumbers(3, 3);
console.log(result1); // Output will be 9

const result2 = divideNumbers(4, 2);
console.log(result2); // Output will be 2